"""
EOL Agent — ServiceNow CMDB Connector
Pulls network device CI records from your ServiceNow instance.
"""

import requests
import json
from typing import Optional
from core.models import Device


class ServiceNowConnector:
    """
    Connects to ServiceNow Table API and pulls network device CIs.

    Usage:
        snow = ServiceNowConnector(
            instance_url = "https://yourco.service-now.com",
            username     = "api_user",
            password     = "secret",
        )
        devices = snow.get_network_devices()
    """

    # CMDB tables to query (in priority order)
    NETWORK_TABLES = [
        "cmdb_ci_netgear",        # Network gear — switches, routers, APs
        "cmdb_ci_ip_switch",      # IP switches
        "cmdb_ci_ip_router",      # Routers
        "cmdb_ci_firewall",       # Firewalls
        "cmdb_ci_wap_hardware",   # Wireless APs
        "cmdb_ci_hardware",       # Generic hardware (fallback)
    ]

    FIELDS = [
        "sys_id", "name", "model_id", "model_number",
        "serial_number", "ip_address", "location",
        "assigned_to", "manufacturer", "short_description",
        "quantity", "u_part_code",
    ]

    def __init__(
        self,
        instance_url: str,
        username: str,
        password: str,
        category_filter: str = "Network",
        table: Optional[str] = None,
    ):
        self.base_url = instance_url.rstrip("/")
        self.auth     = (username, password)
        self.category = category_filter
        self.table    = table   # override table if set
        self.session  = requests.Session()
        self.session.auth    = self.auth
        self.session.headers = {
            "Accept":       "application/json",
            "Content-Type": "application/json",
        }

    # ------------------------------------------------------------------ #
    #  Public API                                                          #
    # ------------------------------------------------------------------ #

    def test_connection(self) -> dict:
        """Quick ping to verify credentials and connectivity."""
        url = f"{self.base_url}/api/now/table/sys_properties"
        params = {"sysparm_limit": 1, "sysparm_fields": "name"}
        try:
            r = self.session.get(url, params=params, timeout=10)
            r.raise_for_status()
            return {"status": "ok", "code": r.status_code}
        except requests.exceptions.ConnectionError:
            return {"status": "error", "message": "Cannot reach ServiceNow — check instance URL"}
        except requests.exceptions.HTTPError as e:
            return {"status": "error", "message": f"HTTP {r.status_code} — check credentials"}
        except Exception as e:
            return {"status": "error", "message": str(e)}

    def get_network_devices(self, limit: int = 1000) -> list[Device]:
        """Pull all network CIs. Tries each CMDB table until results are found."""
        tables = [self.table] if self.table else self.NETWORK_TABLES
        all_devices: list[Device] = []
        seen_ids: set[str] = set()

        for table in tables:
            try:
                records = self._query_table(table, limit)
                for r in records:
                    sys_id = r.get("sys_id", {})
                    if isinstance(sys_id, dict):
                        sys_id = sys_id.get("value", "")
                    if sys_id in seen_ids:
                        continue
                    seen_ids.add(sys_id)
                    device = self._record_to_device(r)
                    if device:
                        all_devices.append(device)
            except Exception as e:
                print(f"  [ServiceNow] Skipping table {table}: {e}")
                continue

        print(f"[ServiceNow] Retrieved {len(all_devices)} devices from CMDB")
        return all_devices

    def update_eol_status(self, sys_id: str, eol_status: str, replacement: str = "") -> bool:
        """
        Write EOL status back to a custom field on the CI record.
        Requires: custom fields u_eol_status, u_replacement_sku on CMDB table.
        """
        url = f"{self.base_url}/api/now/table/cmdb_ci/{sys_id}"
        payload = {
            "u_eol_status":     eol_status,
            "u_replacement_sku": replacement,
        }
        try:
            r = self.session.patch(url, json=payload, timeout=10)
            r.raise_for_status()
            return True
        except Exception as e:
            print(f"  [ServiceNow] Could not update CI {sys_id}: {e}")
            return False

    # ------------------------------------------------------------------ #
    #  Internal helpers                                                    #
    # ------------------------------------------------------------------ #

    def _query_table(self, table: str, limit: int) -> list[dict]:
        url = f"{self.base_url}/api/now/table/{table}"
        params = {
            "sysparm_limit":  limit,
            "sysparm_fields": ",".join(self.FIELDS),
            "sysparm_query":  "active=true",
        }
        r = self.session.get(url, params=params, timeout=30)
        r.raise_for_status()
        return r.json().get("result", [])

    def _val(self, field) -> str:
        """ServiceNow returns fields as {value, display_value} dicts or plain strings."""
        if isinstance(field, dict):
            return field.get("display_value") or field.get("value") or ""
        return str(field) if field else ""

    def _record_to_device(self, r: dict) -> Optional[Device]:
        name      = self._val(r.get("name"))
        part_code = (
            self._val(r.get("u_part_code")) or
            self._val(r.get("model_number")) or
            self._val(r.get("model_id"))
        )
        vendor = (
            self._val(r.get("manufacturer")) or
            self._infer_vendor(part_code, name)
        )
        if not part_code and not name:
            return None

        return Device(
            sys_id      = self._val(r.get("sys_id")),
            name        = name,
            part_code   = part_code or name,
            vendor      = vendor,
            model       = self._val(r.get("short_description")) or name,
            serial      = self._val(r.get("serial_number")),
            ip_address  = self._val(r.get("ip_address")),
            location    = self._val(r.get("location")),
            assigned_to = self._val(r.get("assigned_to")),
            quantity    = int(self._val(r.get("quantity")) or 1),
        )

    def _infer_vendor(self, part_code: str, name: str) -> str:
        from core.researcher import infer_vendor
        return infer_vendor(part_code, name)


